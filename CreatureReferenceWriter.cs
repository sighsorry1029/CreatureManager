    private static void AppendLevelVisualReferenceEntry(StringBuilder builder, GameObject prefab)
    {
        LevelEffects levelEffects = prefab.GetComponentInChildren<LevelEffects>(true);
        if (levelEffects?.m_levelSetups == null || levelEffects.m_levelSetups.Count == 0)
        {
            return;
        }

        AppendListEntry(builder, 0, "prefab", prefab.name);
        AppendLine(builder, 1, "levelEffects:");
        AppendLine(builder, 2, $"component: {FormatYamlString(GetObjectPath(prefab.transform, levelEffects.transform))}");
        AppendLine(builder, 2, $"mainRenderer: {FormatYamlString(GetObjectPath(prefab.transform, levelEffects.m_mainRender != null ? levelEffects.m_mainRender.transform : null))}");
        AppendLine(builder, 2, $"baseEnableObject: {FormatYamlString(GetObjectPath(prefab.transform, levelEffects.m_baseEnableObject != null ? levelEffects.m_baseEnableObject.transform : null))}");
        AppendLine(builder, 2, "levels:");

        for (int index = 0; index < levelEffects.m_levelSetups.Count; index++)
        {
            LevelEffects.LevelSetup setup = levelEffects.m_levelSetups[index];
            if (setup == null)
            {
                continue;
            }

            List<string> values = new()
            {
                $"scale: {FormatFloat(setup.m_scale)}",
                $"hsv: {FormatInlineRawList(FormatFloat(setup.m_hue), FormatFloat(setup.m_saturation), FormatFloat(setup.m_value))}"
            };

            if (setup.m_setEmissiveColor)
            {
                values.Add($"emissive: {FormatColorTuple(setup.m_emissiveColor)}");
            }

            string enableObjectPath = GetObjectPath(prefab.transform, setup.m_enableObject != null ? setup.m_enableObject.transform : null);
            if (enableObjectPath.Length > 0)
            {
                values.Add($"enableObject: {FormatYamlString(enableObjectPath)}");
            }

            AppendLine(builder, 3, $"{index + 2}: {{ {string.Join(", ", values)} }}");
        }
    }

