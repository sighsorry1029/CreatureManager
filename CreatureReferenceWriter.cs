    private static string FormatColor(Vector3 color)
    {
        int r = Mathf.RoundToInt(Mathf.Clamp01(color.x) * 255f);
        int g = Mathf.RoundToInt(Mathf.Clamp01(color.y) * 255f);
        int b = Mathf.RoundToInt(Mathf.Clamp01(color.z) * 255f);
        return $"#{r:X2}{g:X2}{b:X2}";
    }

    private static string FormatColorTuple(Color color)
    {
        return FormatInlineRawList(FormatFloat(color.r), FormatFloat(color.g), FormatFloat(color.b), FormatFloat(color.a));
    }

    private static bool IsWhite(Vector3 color)
    {
        return Mathf.Approximately(color.x, 1f) &&
               Mathf.Approximately(color.y, 1f) &&
               Mathf.Approximately(color.z, 1f);
    }

    private static string FormatInlineList(IEnumerable<string> values)
    {
        string[] formattedValues = values.Select(FormatYamlString).ToArray();
        return "[" + string.Join(", ", formattedValues) + "]";
    }

    private static string FormatWeightedPrefabInlineList(IEnumerable<string> prefabNames)
    {
        List<string> orderedNames = new();
        Dictionary<string, int> countsByName = new(StringComparer.Ordinal);
        foreach (string prefabName in prefabNames)
        {
            if (countsByName.TryGetValue(prefabName, out int count))
            {
                countsByName[prefabName] = count + 1;
            }
            else
            {
                orderedNames.Add(prefabName);
                countsByName[prefabName] = 1;
            }
        }

        return FormatInlineList(orderedNames.Select(prefabName =>
            countsByName[prefabName] == 1 && prefabName.IndexOf(':') < 0
                ? prefabName
                : $"{prefabName}:{countsByName[prefabName]}"));
    }

    private static string FormatInlineRawList(params string[] values)
    {
        return "[" + string.Join(", ", values) + "]";
    }

    private static string FormatYamlString(string? value)
    {
        value ??= "";
        if (value.Length == 0)
        {
            return "''";
        }

        bool requiresQuotes =
            char.IsWhiteSpace(value[0]) ||
            char.IsWhiteSpace(value[value.Length - 1]) ||
            value.IndexOfAny(new[] { ':', '#', '{', '}', '[', ']', ',', '\'', '"', '&', '*', '!', '|', '>', '%', '@', '`' }) >= 0 ||
            value[0] == '-' ||
            value[0] == '?' ||
            string.Equals(value, "null", StringComparison.OrdinalIgnoreCase) ||
            string.Equals(value, "true", StringComparison.OrdinalIgnoreCase) ||
            string.Equals(value, "false", StringComparison.OrdinalIgnoreCase);

        return requiresQuotes ? $"'{value.Replace("'", "''")}'" : value;
    }
