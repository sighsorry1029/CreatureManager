    private static void AppendAppearance(StringBuilder builder, GameObject prefab, int indent)
    {
        VisEquipment visEquipment = prefab.GetComponent<VisEquipment>();
        Humanoid humanoid = prefab.GetComponent<Humanoid>();
        if (visEquipment == null && humanoid == null)
        {
            return;
        }

        int modelIndex = visEquipment?.m_modelIndex ?? 0;
        Vector3 skinColor = visEquipment?.m_skinColor ?? Vector3.one;
        Vector3 hairColor = visEquipment?.m_hairColor ?? Vector3.one;
        string hair = FirstNonEmpty(humanoid?.m_hairItem, visEquipment?.m_hairItem);
        string beard = FirstNonEmpty(humanoid?.m_beardItem, visEquipment?.m_beardItem);
        bool hasHairColor = !IsWhite(hairColor);
        bool hasSkinColor = !IsWhite(skinColor);
        if (hair.Length == 0 && beard.Length == 0 && !hasHairColor && !hasSkinColor && modelIndex == 0)
        {
            return;
        }

        AppendLine(builder, indent, "appearance:");
        if (hair.Length > 0)
        {
            AppendLine(builder, indent + 1, $"hair: {FormatYamlString(hair)}");
        }

        if (beard.Length > 0)
        {
            AppendLine(builder, indent + 1, $"beard: {FormatYamlString(beard)}");
        }

        if (hasHairColor)
        {
            AppendLine(builder, indent + 1, $"hairColor: {FormatYamlString(FormatColor(hairColor))}");
        }

        if (hasSkinColor)
        {
            AppendLine(builder, indent + 1, $"skinColor: {FormatYamlString(FormatColor(skinColor))}");
        }

        if (modelIndex != 0)
        {
            AppendLine(builder, indent + 1, $"modelIndex: {modelIndex.ToString(CultureInfo.InvariantCulture)}");
        }
    }

    private static string FirstNonEmpty(string? primary, string? fallback)
    {
        return !string.IsNullOrWhiteSpace(primary)
            ? primary!
            : !string.IsNullOrWhiteSpace(fallback)
                ? fallback!
                : "";
    }

    private static List<string> GetPrimaryTextureSlots(GameObject prefab)
    {
        Renderer? renderer = FindPrimaryRenderer(prefab);
        if (renderer == null)
        {
            return new List<string>();
        }

        List<string> slots = new();
        Material[] materials = renderer.sharedMaterials;
        for (int index = 0; index < materials.Length; index++)
        {
            if (TryGetMainTexture(materials[index], out Texture texture))
            {
                slots.Add($"{renderer.name}:{index}:{texture.name}");
            }
        }

        return slots;
    }

    private static Renderer? FindPrimaryRenderer(GameObject prefab)
    {
        return prefab
            .GetComponentsInChildren<Renderer>(true)
            .Where(renderer => renderer != null && HasTextureMaterial(renderer) && !IsEffectRenderer(renderer))
            .Select(renderer => new { Renderer = renderer, Score = GetPrimaryRendererScore(prefab, renderer) })
            .Where(entry => entry.Score > int.MinValue / 2)
            .OrderByDescending(entry => entry.Score)
            .ThenBy(entry => GetTransformPath(prefab.transform, entry.Renderer.transform), StringComparer.OrdinalIgnoreCase)
            .Select(entry => entry.Renderer)
            .FirstOrDefault();
    }

    private static bool HasTextureMaterial(Renderer renderer)
    {
        return renderer.sharedMaterials.Any(material => TryGetMainTexture(material, out _));
    }

    private static bool IsEffectRenderer(Renderer renderer)
    {
        string rendererTypeName = renderer.GetType().Name;
        if (string.Equals(rendererTypeName, "ParticleSystemRenderer", StringComparison.Ordinal) ||
            string.Equals(rendererTypeName, "TrailRenderer", StringComparison.Ordinal) ||
            string.Equals(rendererTypeName, "LineRenderer", StringComparison.Ordinal))
        {
            return true;
        }

        string text = (renderer.name + "/" + GetTransformPath(null, renderer.transform)).ToLowerInvariant();
        string[] effectTokens =
        {
            "fx", "vfx", "sfx", "particle", "particles", "smoke", "mist", "splash",
            "splsh", "flies", "ooz", "wet", "water", "dust", "fire", "flame", "glow"
        };

        return effectTokens.Any(token => text.Contains(token));
    }

    private static int GetPrimaryRendererScore(GameObject prefab, Renderer renderer)
    {
        string rendererName = renderer.name ?? "";
        string path = GetTransformPath(prefab.transform, renderer.transform);
        string searchable = (rendererName + "/" + path).ToLowerInvariant();
        string prefabName = prefab.name ?? "";
        int score = 0;

        if (renderer is SkinnedMeshRenderer)
        {
            score += 100;
        }

        if (string.Equals(rendererName, prefabName, StringComparison.OrdinalIgnoreCase))
        {
            score += 120;
        }
        else if (prefabName.Length > 0 && searchable.Contains(prefabName.ToLowerInvariant()))
        {
            score += 80;
        }

        if (ContainsAny(searchable, "body", "character", "model", "mesh", "main", "root"))
        {
            score += 50;
        }

        if (ContainsAny(searchable, "lod", "ragdoll", "collider", "shadow"))
        {
            score -= 40;
        }

        return score;
    }

    private static bool ContainsAny(string value, params string[] tokens)
    {
        return tokens.Any(token => value.Contains(token));
    }

    private static string GetTransformPath(Transform? root, Transform transform)
    {
        Stack<string> parts = new();
        Transform? current = transform;
        while (current != null && current != root)
        {
            parts.Push(current.name);
            current = current.parent;
        }

        return string.Join("/", parts);
    }

    private static string GetObjectPath(Transform root, Transform? transform)
    {
        if (transform == null)
        {
            return "";
        }

        string path = GetTransformPath(root, transform);
        return string.IsNullOrWhiteSpace(path) ? "." : path;
    }

    private static bool TryGetMainTexture(Material? material, out Texture texture)
    {
        texture = null!;
        if (material == null || !material.HasProperty(MainTextureProperty))
        {
            return false;
        }

        Texture? candidate = material.GetTexture(MainTextureProperty);
        if (candidate == null)
        {
            return false;
        }

        texture = candidate;
        return true;
    }

