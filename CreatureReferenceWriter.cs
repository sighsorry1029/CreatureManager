    internal static string BuildCreatureLoadoutReferenceText()
    {
        CreatureAssetOwnerCatalog.PrepareMappings();
        List<CreatureLoadoutReferenceEntry> entries = GetCreatureLoadoutReferenceEntries();
        if (entries.Count == 0)
        {
            return "[]\n";
        }

        StringBuilder builder = new();
        builder.AppendLine("# Generated by CreatureManager (creature loadout reference).");
        builder.AppendLine("# Lists likely creature/NPC equipment ItemDrop prefabs for humanoid.defaultItems and random* loadout fields.");
        builder.AppendLine("# Pure attack holder prefabs such as attack_*, *_shoot, *_shot, and *_melee are omitted.");
        builder.AppendLine("# ItemDrop prefabs with attack animations but no visible renderer are omitted as likely hidden attack holders.");
        builder.AppendLine("# Craftable recipe items are omitted as likely player items.");
        builder.AppendLine("# usedBy is gathered from non-player creature Humanoid loadout fields.");
        builder.AppendLine("# Owner sections are best-effort guesses from the Valheim manifest and loaded asset bundles.");
        builder.AppendLine();

        bool wroteSection = false;
        foreach (IGrouping<string, CreatureLoadoutReferenceEntry> section in entries
                     .OrderBy(entry => CreatureReferenceSections.GetOwnerSortBucket(entry.OwnerName))
                     .ThenBy(entry => entry.OwnerName, StringComparer.OrdinalIgnoreCase)
                     .ThenBy(entry => entry.Name, StringComparer.OrdinalIgnoreCase)
                     .GroupBy(entry => entry.OwnerName, StringComparer.OrdinalIgnoreCase))
        {
            if (wroteSection)
            {
                builder.AppendLine();
            }

            builder.Append("# ===== ");
            builder.Append(section.Key);
            builder.AppendLine(" =====");

            bool wroteEntry = false;
            foreach (CreatureLoadoutReferenceEntry entry in section)
            {
                if (wroteEntry)
                {
                    builder.AppendLine();
                }

                builder.AppendLine(entry.Name);
                builder.AppendLine($"  usedBy: {FormatInlineList(entry.UsedBy)}");
                if (!string.IsNullOrWhiteSpace(entry.AttackAnimation))
                {
                    builder.AppendLine($"  attackAnimation: {FormatYamlString(entry.AttackAnimation)}");
                }
                wroteEntry = true;
            }

            wroteSection = true;
        }

        return builder.ToString();
    }

    internal static string BuildProjectileReferenceYaml()
    {
        CreatureAssetOwnerCatalog.PrepareMappings();
        List<ProjectileReferenceEntry> entries = GetProjectileReferenceEntries();
        if (entries.Count == 0)
        {
            return "[]\n";
        }

        StringBuilder builder = new();
        builder.AppendLine("# Generated by CreatureManager (projectile reference).");
        builder.AppendLine("# Lists projectile-like prefabs that can be inspected or copied into projectile.yml.");
        builder.AppendLine("# Includes prefabs with root Projectile, Aoe, TriggerSpawnAbility, or SpawnAbility components, plus prefabs referenced by ItemDrop attack projectile fields.");
        builder.AppendLine("# usedByAttacks is reference-only metadata gathered from loaded ItemDrop primary and secondary attacks; projectile.yml ignores it.");
        builder.AppendLine("# projectile and spawnAbility blocks are emitted only when those components exist on the prefab root.");
        builder.AppendLine("# Repeated spawnAbility.spawnPrefabs entries are compacted as Prefab:weight; an omitted weight means 1.");
        builder.AppendLine("# Owner sections are best-effort guesses from the Valheim manifest and loaded asset bundles.");
        builder.AppendLine();

        bool wroteSection = false;
        foreach (IGrouping<string, ProjectileReferenceEntry> section in entries
                     .OrderBy(entry => CreatureReferenceSections.GetOwnerSortBucket(entry.OwnerName))
                     .ThenBy(entry => entry.OwnerName, StringComparer.OrdinalIgnoreCase)
                     .ThenBy(entry => entry.Name, StringComparer.OrdinalIgnoreCase)
                     .GroupBy(entry => entry.OwnerName, StringComparer.OrdinalIgnoreCase))
        {
            if (wroteSection)
            {
                builder.AppendLine();
            }

            builder.Append("# ===== ");
            builder.Append(section.Key);
            builder.AppendLine(" =====");

            bool wroteEntry = false;
            foreach (ProjectileReferenceEntry entry in section)
            {
                if (wroteEntry)
                {
                    builder.AppendLine();
                }

                AppendListEntry(builder, 0, "prefab", entry.Name);
                AppendLine(builder, 1, $"usedByAttacks: {FormatInlineList(entry.UsedByAttacks)}");
                if (entry.HasProjectile)
                {
                    AppendLine(builder, 1, "projectile:");
                    AppendLine(builder, 2, $"spawnOnHit: {(entry.ProjectileSpawnOnHit == null ? "null" : FormatYamlString(entry.ProjectileSpawnOnHit))}");
                }

                if (entry.HasSpawnAbility)
                {
                    AppendLine(builder, 1, "spawnAbility:");
                    AppendLine(builder, 2, $"spawnPrefabs: {FormatWeightedPrefabInlineList(entry.SpawnAbilityPrefabs)}");
                }

                wroteEntry = true;
            }

            wroteSection = true;
        }

        return builder.ToString();
    }

    private sealed class CreatureLoadoutUsage
    {
        public Dictionary<string, HashSet<string>> UsedBy { get; } = new(StringComparer.OrdinalIgnoreCase);

        public void Add(string creatureName, string slot)
        {
            if (!UsedBy.TryGetValue(creatureName, out HashSet<string> slots))
            {
                slots = new HashSet<string>(StringComparer.OrdinalIgnoreCase);
                UsedBy[creatureName] = slots;
            }

            slots.Add(slot);
        }

        public List<string> FormatUsedBy()
        {
            return UsedBy
                .OrderBy(entry => entry.Key, StringComparer.OrdinalIgnoreCase)
                .Select(entry => $"{entry.Key}({string.Join(", ", entry.Value.OrderBy(slot => slot, StringComparer.OrdinalIgnoreCase))})")
                .ToList();
        }

    }

    private sealed class CreatureLoadoutReferenceEntry
    {
        public string Name { get; set; } = "";
        public string OwnerName { get; set; } = CreatureReferenceSections.UnknownOwnerName;
        public string AttackAnimation { get; set; } = "";
        public List<string> UsedBy { get; set; } = new();
    }

    private sealed class ProjectileReferenceUsage
    {
        public GameObject? Prefab { get; set; }
        public HashSet<string> UsedByAttacks { get; } = new(StringComparer.OrdinalIgnoreCase);
    }

    private sealed class ProjectileReferenceEntry
    {
        public string Name { get; set; } = "";
        public string OwnerName { get; set; } = CreatureReferenceSections.UnknownOwnerName;
        public List<string> UsedByAttacks { get; set; } = new();
        public bool HasProjectile { get; set; }
        public string? ProjectileSpawnOnHit { get; set; }
        public bool HasSpawnAbility { get; set; }
        public List<string> SpawnAbilityPrefabs { get; set; } = new();
    }

    private static List<CreatureLoadoutReferenceEntry> GetCreatureLoadoutReferenceEntries()
    {
        Dictionary<string, CreatureLoadoutUsage> usageByItem = CollectCreatureLoadoutUsage();
        HashSet<string> craftableItems = GetCraftableRecipeItemNames();
        List<CreatureLoadoutReferenceEntry> entries = new();

        foreach (GameObject itemPrefab in CreaturePrefabRegistry.GetItemPrefabs())
        {
            if (itemPrefab == null)
            {
                continue;
            }

            string itemName = itemPrefab.name ?? "";
            if (itemName.Length == 0)
            {
                continue;
            }

            ItemDrop? itemDrop = itemPrefab.GetComponent<ItemDrop>();
            if (itemDrop == null)
            {
                continue;
            }

            ItemDrop.ItemData.SharedData shared = itemDrop.m_itemData.m_shared;

            bool hasRecipe = craftableItems.Contains(itemName);
            usageByItem.TryGetValue(itemName, out CreatureLoadoutUsage usage);
            if (!ShouldIncludeCreatureLoadoutItem(itemPrefab, shared, usage, hasRecipe))
            {
                continue;
            }

            entries.Add(new CreatureLoadoutReferenceEntry
            {
                Name = itemName,
                OwnerName = CreaturePrefabOwnerResolver.GetOwnerName(itemName),
                AttackAnimation = GetAttackAnimation(itemPrefab) ?? "",
                UsedBy = usage?.FormatUsedBy() ?? new List<string>()
            });
        }

        return entries;
    }

    private static List<ProjectileReferenceEntry> GetProjectileReferenceEntries()
    {
        Dictionary<string, ProjectileReferenceUsage> usageByProjectile = CollectProjectileUsage();
        Dictionary<string, GameObject> prefabsByName = new(StringComparer.OrdinalIgnoreCase);

        foreach (GameObject prefab in CreaturePrefabRegistry.GetProjectilePrefabs())
        {
            if (prefab != null && !string.IsNullOrWhiteSpace(prefab.name))
            {
                prefabsByName[prefab.name] = prefab;
            }
        }

        foreach (SpawnAbility spawnAbility in Resources.FindObjectsOfTypeAll<SpawnAbility>())
        {
            GameObject? candidate = spawnAbility?.gameObject;
            if (candidate == null || string.IsNullOrWhiteSpace(candidate.name))
            {
                continue;
            }

            GameObject? resolved = CreaturePrefabRegistry.GetPrefab(candidate.name);
            if (resolved != null && resolved.GetComponent<SpawnAbility>() != null)
            {
                prefabsByName[resolved.name] = resolved;
            }
        }

        foreach (KeyValuePair<string, ProjectileReferenceUsage> entry in usageByProjectile)
        {
            if (entry.Value.Prefab != null)
            {
                prefabsByName[entry.Key] = entry.Value.Prefab;
                continue;
            }

            GameObject? resolved = CreaturePrefabRegistry.GetPrefab(entry.Key);
            if (resolved != null)
            {
                prefabsByName[entry.Key] = resolved;
            }
        }

        return prefabsByName
            .OrderBy(entry => entry.Key, StringComparer.OrdinalIgnoreCase)
            .Select(entry =>
            {
                usageByProjectile.TryGetValue(entry.Key, out ProjectileReferenceUsage usage);
                Projectile? projectile = entry.Value.GetComponent<Projectile>();
                SpawnAbility? spawnAbility = entry.Value.GetComponent<SpawnAbility>();
                return new ProjectileReferenceEntry
                {
                    Name = entry.Key,
                    OwnerName = CreaturePrefabOwnerResolver.GetOwnerName(entry.Key),
                    UsedByAttacks = usage?.UsedByAttacks
                        .OrderBy(name => name, StringComparer.OrdinalIgnoreCase)
                        .ToList() ?? new List<string>(),
                    HasProjectile = projectile != null,
                    ProjectileSpawnOnHit = GetPrefabNameOrNull(projectile?.m_spawnOnHit),
                    HasSpawnAbility = spawnAbility != null,
                    SpawnAbilityPrefabs = spawnAbility?.m_spawnPrefab?
                        .Where(prefab => prefab != null && !string.IsNullOrWhiteSpace(prefab.name))
                        .Select(prefab => prefab.name)
                        .ToList() ?? new List<string>()
                };
            })
            .ToList();
    }

    private static Dictionary<string, CreatureLoadoutUsage> CollectCreatureLoadoutUsage()
    {
        Dictionary<string, CreatureLoadoutUsage> usageByItem = new(StringComparer.OrdinalIgnoreCase);
        foreach (GameObject creaturePrefab in CreaturePrefabRegistry.GetCreaturePrefabs())
        {
            Humanoid humanoid = creaturePrefab.GetComponent<Humanoid>();
            if (humanoid == null)
            {
                continue;
            }

            AddLoadoutItems(usageByItem, creaturePrefab.name, "defaultItems", humanoid.m_defaultItems);
            AddLoadoutItems(usageByItem, creaturePrefab.name, "randomWeapon", humanoid.m_randomWeapon);
            GetRandomArmorAndHairForOutput(creaturePrefab, humanoid, out GameObject[] randomArmor, out GameObject[] randomHair);
            AddLoadoutItems(usageByItem, creaturePrefab.name, "randomArmor", randomArmor);
            AddLoadoutItems(usageByItem, creaturePrefab.name, "randomHair", randomHair);
            AddLoadoutItems(usageByItem, creaturePrefab.name, "randomShield", humanoid.m_randomShield);
            AddRandomItems(usageByItem, creaturePrefab.name, humanoid.m_randomItems);
            AddRandomSets(usageByItem, creaturePrefab.name, humanoid.m_randomSets);
        }

        return usageByItem;
    }

    private static Dictionary<string, ProjectileReferenceUsage> CollectProjectileUsage()
    {
        Dictionary<string, ProjectileReferenceUsage> usageByProjectile = new(StringComparer.OrdinalIgnoreCase);
        foreach (GameObject itemPrefab in CreaturePrefabRegistry.GetItemPrefabs())
        {
            ItemDrop? itemDrop = itemPrefab.GetComponent<ItemDrop>();
            ItemDrop.ItemData.SharedData? shared = itemDrop?.m_itemData?.m_shared;
            if (shared == null)
            {
                continue;
            }

            AddAttackProjectileUsage(usageByProjectile, itemPrefab.name, shared.m_attack);
            AddAttackProjectileUsage(usageByProjectile, $"{itemPrefab.name}:secondary", shared.m_secondaryAttack);
        }

        return usageByProjectile;
    }

    private static void AddAttackProjectileUsage(Dictionary<string, ProjectileReferenceUsage> usageByProjectile, string attackName, Attack? attack)
    {
        GameObject? projectilePrefab = attack?.m_attackProjectile;
        string projectileName = projectilePrefab?.name ?? "";
        if (projectileName.Length == 0)
        {
            return;
        }

        if (!usageByProjectile.TryGetValue(projectileName, out ProjectileReferenceUsage usage))
        {
            usage = new ProjectileReferenceUsage();
            usageByProjectile[projectileName] = usage;
        }

        usage.Prefab ??= projectilePrefab;
        usage.UsedByAttacks.Add(attackName);
    }

    private static string? GetPrefabNameOrNull(GameObject? prefab)
    {
        string name = prefab?.name ?? "";
        return string.IsNullOrWhiteSpace(name) ? null : name;
    }

    private static void AddLoadoutItems(Dictionary<string, CreatureLoadoutUsage> usageByItem, string creatureName, string slot, IEnumerable<GameObject>? items)
    {
        if (items == null)
        {
            return;
        }

        foreach (GameObject item in items)
        {
            AddLoadoutItem(usageByItem, creatureName, slot, item);
        }
    }

    private static void AddRandomItems(Dictionary<string, CreatureLoadoutUsage> usageByItem, string creatureName, Humanoid.RandomItem[]? items)
    {
        if (items == null)
        {
            return;
        }

        foreach (Humanoid.RandomItem item in items)
        {
            AddLoadoutItem(usageByItem, creatureName, "randomItems", item?.m_prefab);
        }
    }

    private static void AddRandomSets(Dictionary<string, CreatureLoadoutUsage> usageByItem, string creatureName, Humanoid.ItemSet[]? sets)
    {
        if (sets == null)
        {
            return;
        }

        foreach (Humanoid.ItemSet set in sets)
        {
            AddLoadoutItems(usageByItem, creatureName, $"randomSets:{set?.m_name ?? ""}", set?.m_items);
        }
    }

    private static void AddLoadoutItem(Dictionary<string, CreatureLoadoutUsage> usageByItem, string creatureName, string slot, GameObject? item)
    {
        if (item == null || item.GetComponent<ItemDrop>() == null)
        {
            return;
        }

        string itemName = item.name ?? "";
        if (itemName.Length == 0)
        {
            return;
        }

        if (!usageByItem.TryGetValue(itemName, out CreatureLoadoutUsage usage))
        {
            usage = new CreatureLoadoutUsage();
            usageByItem[itemName] = usage;
        }

        usage.Add(creatureName, slot);
    }

    private static HashSet<string> GetCraftableRecipeItemNames()
    {
        HashSet<string> items = new(StringComparer.OrdinalIgnoreCase);
        if (ObjectDB.instance?.m_recipes == null)
        {
            return items;
        }

        foreach (Recipe recipe in ObjectDB.instance.m_recipes)
        {
            if (recipe?.m_item == null)
            {
                continue;
            }

            string itemName = recipe.m_item.gameObject?.name ?? "";
            if (itemName.Length > 0)
            {
                items.Add(itemName);
            }
        }

        return items;
    }

    private static bool ShouldIncludeCreatureLoadoutItem(GameObject itemPrefab, ItemDrop.ItemData.SharedData shared, CreatureLoadoutUsage? usage, bool hasRecipe)
    {
        if (hasRecipe)
        {
            return false;
        }

        if (IsLikelyAttackOnlyItem(itemPrefab, shared))
        {
            return false;
        }

        return IsCreatureLoadoutItemType(shared.m_itemType.ToString()) || usage != null;
    }

    private static bool IsCreatureLoadoutItemType(string itemType)
    {
        return itemType is
            "OneHandedWeapon" or
            "TwoHandedWeapon" or
            "TwoHandedWeaponLeft" or
            "Bow" or
            "Shield" or
            "Helmet" or
            "Chest" or
            "Legs" or
            "Shoulder" or
            "Utility" or
            "Tool" or
            "Torch" or
            "Attach_Atgeir";
    }

    private static bool IsLikelyAttackOnlyItem(GameObject itemPrefab, ItemDrop.ItemData.SharedData shared)
    {
        string name = (itemPrefab.name ?? "").ToLowerInvariant();
        if (name.StartsWith("attack_", StringComparison.Ordinal) ||
            name.Contains("_attack") ||
            name.EndsWith("_shoot", StringComparison.Ordinal) ||
            name.EndsWith("_shot", StringComparison.Ordinal) ||
            name.EndsWith("_melee", StringComparison.Ordinal) ||
            name.Contains("projectile"))
        {
            return true;
        }

        string attackAnimation = GetAttackAnimation(itemPrefab) ?? "";
        string itemType = shared.m_itemType.ToString();
        if (attackAnimation.Length == 0)
        {
            return false;
        }

        if (!HasVisibleRenderer(itemPrefab))
        {
            return true;
        }

        if (string.Equals(itemType, "None", StringComparison.OrdinalIgnoreCase))
        {
            return true;
        }

        return !IsCreatureLoadoutItemType(itemType) && !HasItemIcon(shared) && !HasVisibleRenderer(itemPrefab);
    }

    private static bool HasItemIcon(ItemDrop.ItemData.SharedData shared)
    {
        return shared.m_icons != null && shared.m_icons.Any(icon => icon != null);
    }

    private static bool HasVisibleRenderer(GameObject prefab)
    {
        return prefab.GetComponentsInChildren<Renderer>(true)
            .Any(renderer => renderer != null && !IsEffectRenderer(renderer));
    }

    private static List<GameObject> GetAiPrefabs()
    {
        return CreaturePrefabRegistry.GetCreaturePrefabs()
            .Where(prefab => prefab.GetComponent<MonsterAI>() != null || prefab.GetComponent<AnimalAI>() != null)
            .ToList();
    }

    private static List<GameObject> GetLevelVisualPrefabs()
    {
        return CreaturePrefabRegistry.GetCreaturePrefabs()
            .Where(prefab =>
            {
                LevelEffects levelEffects = prefab.GetComponentInChildren<LevelEffects>(true);
                return levelEffects?.m_levelSetups != null && levelEffects.m_levelSetups.Count > 0;
            })
            .ToList();
    }

    private static HashSet<string> GetKnownAttackAnimationNames()
    {
        return CreaturePrefabRegistry.GetAttackPrefabs()
            .Select(GetAttackAnimation)
            .Where(animation => !string.IsNullOrWhiteSpace(animation))
            .Select(animation => animation!)
            .ToHashSet(StringComparer.OrdinalIgnoreCase);
    }

    private static List<string> GetAvailableAttackAnimations(GameObject prefab, HashSet<string> knownAttackAnimationNames)
    {
        List<string> animatorParameters = GetAnimationParameters(prefab);
        if (animatorParameters.Count == 0 || knownAttackAnimationNames.Count == 0)
        {
            return GetConfiguredAttackAnimations(prefab);
        }

        List<string> availableAnimations = animatorParameters
            .Where(knownAttackAnimationNames.Contains)
            .Distinct(StringComparer.OrdinalIgnoreCase)
            .OrderBy(name => name, StringComparer.OrdinalIgnoreCase)
            .ToList();

        return availableAnimations.Count > 0 ? availableAnimations : GetConfiguredAttackAnimations(prefab);
    }

    private static List<string> GetConfiguredAttackAnimations(GameObject prefab)
    {
        Humanoid humanoid = prefab.GetComponent<Humanoid>();
        return humanoid?.m_defaultItems?
            .Where(item => item != null)
            .Select(GetAttackAnimation)
            .Where(animation => !string.IsNullOrWhiteSpace(animation))
            .Select(animation => animation!)
            .Distinct(StringComparer.OrdinalIgnoreCase)
            .OrderBy(animation => animation, StringComparer.OrdinalIgnoreCase)
            .ToList() ?? new List<string>();
    }

    private static List<string> GetAnimationParameters(GameObject prefab)
    {
        List<string> directParameters = GetAnimatorParameterNames(
            prefab.GetComponent<Character>()?.m_animator ??
            prefab.GetComponentInChildren<Animator>(true));
        if (directParameters.Count > 0)
        {
            return directParameters;
        }

        return GetInstantiatedAnimatorParameterNames(prefab);
    }

    private static List<string> GetAnimatorParameterNames(Animator? animator)
    {
        if (animator == null)
        {
            return new List<string>();
        }

        return animator.parameters
            .Select(parameter => parameter.name)
            .Where(name => !string.IsNullOrWhiteSpace(name))
            .Distinct(StringComparer.OrdinalIgnoreCase)
            .OrderBy(name => name, StringComparer.OrdinalIgnoreCase)
            .ToList();
    }

    private static List<string> GetInstantiatedAnimatorParameterNames(GameObject prefab)
    {
        if (prefab.GetComponentInChildren<Animator>(true) == null)
        {
            return new List<string>();
        }

        GameObject? instance = null;
        bool previousForceDisableInit = ZNetView.m_forceDisableInit;
        try
        {
            ZNetView.m_forceDisableInit = true;
            instance = UnityEngine.Object.Instantiate(prefab);
            return GetAnimatorParameterNames(instance.GetComponentInChildren<Animator>(true));
        }
        catch (Exception ex)
        {
            CreatureManagerPlugin.Log.LogDebug($"Failed to inspect animation parameters for '{prefab.name}': {ex.Message}");
            return new List<string>();
        }
        finally
        {
            ZNetView.m_forceDisableInit = previousForceDisableInit;
            if (instance != null)
            {
                UnityEngine.Object.DestroyImmediate(instance);
            }
        }
    }

    private static string BuildSectionedPrefabYaml(
        List<GameObject> prefabs,
        string artifactKind,
        Action<StringBuilder, GameObject> appendEntry)
    {
        CreatureAssetOwnerCatalog.PrepareMappings();
        if (prefabs.Count == 0)
        {
            return "[]\n";
        }

        StringBuilder builder = new();
        AppendGeneratedHeader(builder, artifactKind);
        CreatureReferenceSections.AppendPrefabSections(builder, prefabs, appendEntry);
        return builder.ToString();
    }

    private static void AppendReferenceEntry(StringBuilder builder, GameObject prefab)
    {
        Character character = prefab.GetComponent<Character>();
        AppendListEntry(builder, 0, "prefab", prefab.name);
        AppendLine(builder, 1, "character:");
        AppendLine(builder, 2, $"name: {FormatYamlString(character.m_name)}");
        AppendLine(builder, 2, $"faction: {CreatureFactionManager.GetDisplayName(character.m_faction)}");
        if (character.m_boss)
        {
            AppendLine(builder, 2, $"boss: {FormatBossTuple(character)}");
        }

        AppendLine(builder, 2, $"health: {FormatFloat(character.m_health)}");
        AppendReferenceDamageModifiers(builder, character.m_damageModifiers, 2);
    }

    private static void AppendFullScaffoldEntry(StringBuilder builder, GameObject prefab, HashSet<string> attackAnimationNames)
    {
        Character character = prefab.GetComponent<Character>();
        AppendListEntry(builder, 0, "prefab", prefab.name);
        AppendLine(builder, 1, "character:");
        AppendLine(builder, 2, $"name: {FormatYamlString(character.m_name)}");
        AppendLine(builder, 2, $"faction: {CreatureFactionManager.GetDisplayName(character.m_faction)}");
        AppendLine(builder, 2, $"boss: {FormatBossTuple(character)}");
        AppendLine(builder, 2, $"defeatSetGlobalKey: {FormatYamlString(character.m_defeatSetGlobalKey)}");
        AppendLine(builder, 2, $"health: {FormatTuple(character.m_health, character.m_regenAllHPTime)}");
        AppendLine(builder, 2, "damageModifiers:");
        AppendDamageModifiers(builder, character.m_damageModifiers, 3);
        AppendLine(builder, 2, $"speed: {FormatTuple(character.m_crouchSpeed, character.m_walkSpeed, character.m_speed, character.m_turnSpeed, character.m_runSpeed, character.m_runTurnSpeed, character.m_acceleration)}");
        AppendLine(builder, 2, $"jump: {FormatTuple(character.m_jumpForce, character.m_jumpForceForward, character.m_jumpForceTiredFactor, character.m_airControl, character.m_jumpStaminaUsage)}");
        AppendLine(builder, 2, $"swim: {FormatTuple(character.m_canSwim, character.m_swimDepth, character.m_swimSpeed, character.m_swimTurnSpeed, character.m_swimAcceleration)}");
        AppendLine(builder, 2, $"flight: {FormatTuple(character.m_flying, character.m_flySlowSpeed, character.m_flyFastSpeed, character.m_flyTurnSpeed)}");
        if (prefab.GetComponent<MonsterAI>() != null || prefab.GetComponent<AnimalAI>() != null)
        {
            AppendLine(builder, 1, $"ai: {FormatYamlString(prefab.name)}");
        }

        AppendHumanoidBlock(builder, prefab, 1);
        AppendLine(builder, 1, $"scale: {FormatFloat(prefab.transform.localScale.x)}");
        AppendLine(builder, 1, $"textures: {FormatInlineList(GetPrimaryTextureSlots(prefab))}");
        AppendAppearance(builder, prefab, 1);
        AppendLine(builder, 1, $"availableAttackAnimations: {FormatInlineList(GetAvailableAttackAnimations(prefab, attackAnimationNames))}");
    }

    private static void AppendAttackReferenceEntry(StringBuilder builder, GameObject prefab)
    {
