    private static void AppendDamageModifiers(StringBuilder builder, HitData.DamageModifiers modifiers, int indent)
    {
        foreach ((string key, HitData.DamageModifier value) in EnumerateDamageModifiers(modifiers))
        {
            AppendLine(builder, indent, $"{key}: {value}");
        }
    }

    private static void AppendReferenceDamageModifiers(StringBuilder builder, HitData.DamageModifiers modifiers, int indent)
    {
        List<(string Key, HitData.DamageModifier Value)> values = EnumerateDamageModifiers(modifiers)
            .Where(entry => !IsReferenceDefaultDamageModifier(entry.Key, entry.Value))
            .ToList();
        if (values.Count == 0)
        {
            return;
        }

        AppendLine(builder, indent, "damageModifiers:");
        foreach ((string key, HitData.DamageModifier value) in values)
        {
            AppendLine(builder, indent + 1, $"{key}: {value}");
        }
    }

    private static bool IsReferenceDefaultDamageModifier(string key, HitData.DamageModifier value)
    {
        if (value == HitData.DamageModifier.Normal)
        {
            return true;
        }

        return (key == "chop" || key == "pickaxe") && value == HitData.DamageModifier.Ignore;
    }

    private static IEnumerable<(string Key, HitData.DamageModifier Value)> EnumerateDamageModifiers(HitData.DamageModifiers modifiers)
    {
        yield return ("blunt", modifiers.m_blunt);
        yield return ("slash", modifiers.m_slash);
        yield return ("pierce", modifiers.m_pierce);
        yield return ("chop", modifiers.m_chop);
        yield return ("pickaxe", modifiers.m_pickaxe);
        yield return ("fire", modifiers.m_fire);
        yield return ("frost", modifiers.m_frost);
        yield return ("lightning", modifiers.m_lightning);
        yield return ("poison", modifiers.m_poison);
        yield return ("spirit", modifiers.m_spirit);
    }

    private static void AppendHumanoidBlock(StringBuilder builder, GameObject prefab, int indent)
    {
        Humanoid humanoid = prefab.GetComponent<Humanoid>();
        if (humanoid == null)
        {
            return;
        }

        AppendLine(builder, indent, "humanoid:");
        AppendDefaultItems(builder, indent + 1, humanoid.m_defaultItems);
        AppendItemList(builder, indent + 1, "randomWeapon", humanoid.m_randomWeapon);
        GetRandomArmorAndHairForOutput(prefab, humanoid, out GameObject[] randomArmor, out GameObject[] randomHair);
        AppendItemList(builder, indent + 1, "randomArmor", randomArmor);
        AppendItemList(builder, indent + 1, "randomHair", randomHair);
        AppendItemList(builder, indent + 1, "randomShield", humanoid.m_randomShield);
        AppendRandomItems(builder, indent + 1, humanoid.m_randomItems);
        AppendRandomSets(builder, indent + 1, humanoid.m_randomSets);
    }

    private static void GetRandomArmorAndHairForOutput(
        GameObject creaturePrefab,
        Humanoid humanoid,
        out GameObject[] randomArmor,
        out GameObject[] randomHair)
    {
        GameObject[] sourceArmor = humanoid.m_randomArmor ?? Array.Empty<GameObject>();
        if (CreatureDomainManager.TryGetRandomHairPrefabs(creaturePrefab, out IReadOnlyList<GameObject> configuredHair))
        {
            randomArmor = sourceArmor;
            randomHair = configuredHair.Where(item => item != null).ToArray();
            return;
        }

        if (CreatureDomainManager.IsConfirmedHairOnly(sourceArmor))
        {
            randomArmor = Array.Empty<GameObject>();
            randomHair = sourceArmor;
            return;
        }

        randomArmor = sourceArmor;
        randomHair = Array.Empty<GameObject>();
    }

    private static void AppendDefaultItems(StringBuilder builder, int indent, GameObject[]? items)
    {
        List<GameObject> itemList = items?
            .Where(item => item != null)
            .GroupBy(item => item.name, StringComparer.OrdinalIgnoreCase)
            .Select(group => group.First())
            .ToList() ?? new List<GameObject>();

        if (itemList.Count == 0)
        {
            AppendLine(builder, indent, "defaultItems: []");
            return;
        }

        List<string> names = itemList
            .OrderByDescending(CreaturePrefabRegistry.IsAttackItem)
            .ThenBy(item => item.name, StringComparer.OrdinalIgnoreCase)
            .Select(item => item.name)
            .ToList();

        AppendLine(builder, indent, "defaultItems:");
        foreach (string name in names)
        {
            AppendLine(builder, indent + 1, $"- {FormatYamlString(name)}");
        }
    }

